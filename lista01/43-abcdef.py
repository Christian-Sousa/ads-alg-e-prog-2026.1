a = float(input("Valor de A: "))
b = float(input("Valor de B: "))
c = float(input("Valor de C: "))
d = float(input("Valor de D: "))
e = float(input("Valor de E: ")) 
f = float(input("Valor de F: ")) 
x = (c*e - b*f)/(a*e - b*d)
y = (a*f - c*d)/(a*e - b*d)
print(f" > X = {x:.2f}")
print(f" > Y = {y:.2f}")